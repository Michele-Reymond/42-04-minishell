#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	len;

	if (*s != 0 && fd != '\0')
	{
		len = 0;
		len = ft_strlen(s);
		if (len != 0)
			write(fd, s, len);
	}
}
